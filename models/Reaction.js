const { Schema, Types } = require('mongoose');
const formatDate = require('../utils/helpers/formatDate');

const reactionSchema = new Schema({
    reactionId: {
        type: Schema.Types.ObjectId,
        default: () => new Types.ObjectId(),
    },
    reactionBody: {
        type: String,
        required: true,
        maxLength: 280
    },
    username: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now,
        get: (v) => formatDate(v)
    }
})

module.exports = reactionSchema;